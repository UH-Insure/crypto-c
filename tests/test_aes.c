#include <stdio.h>
#include <string.h>
#include <stdint.h>
#include "primitives/aes.h"

int main(void) {
    // FIPS-197, C.1 test vector
    uint8_t key[16] = {
        0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
        0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
    };
    uint8_t pt[16] = {
        0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,
        0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff
    };
    uint8_t ct_expected[16] = {
        0x69,0xc4,0xe0,0xd8,0x6a,0x7b,0x04,0x30,
        0xd8,0xcd,0xb7,0x80,0x70,0xb4,0xc5,0x5a
    };

    uint8_t ct[16], dec[16];

    aes128_ctx ctx;
    aes128_set_key(&ctx, key);

    aes128_encrypt_block(&ctx, pt, ct);
    if (memcmp(ct, ct_expected, 16) != 0) {
        puts("[test_aes] encrypt mismatch");
        return 1;
    }

    aes128_decrypt_block(&ctx, ct, dec);
    if (memcmp(dec, pt, 16) != 0) {
        puts("[test_aes] decrypt mismatch");
        return 1;
    }

    puts("[test_aes] ok");
    return 0;
}
